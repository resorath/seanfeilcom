			<div class="portfolio-story">
				<p>Active Living is the University of Calgary's health-focused department, offering a massive directory of indoor and outdoor fitness programs and activities. The department started online registrations in 2005 under the Faculty of Kinesiology. Since then they have undergone significant evolution both in their course offering and their online presence.</p>

				<p>Staying on top of the fitness game, Active Living sought a brand new system for online registration, which brought in the bulk of their customers. The existing information architecture was stripped to the skeleton, and ideas for a new architecture was brought to the department's leadership. The new design would have required some significant staff training, business model changes, and several interations of customer testing; however it was green lit and the wireframes started.</p>

				<div class="portfolio-inline-img">
					<img src="<?=base_url() ?>content/portfolio/activeliving-admin.png">
					<p class="portfolio-inline-img-caption">
						The new registration system required an all new administrative interface for program coordinators to organize their offerings.
					</p>
				</div>

				<p>The Active Living leadership worked closely with the development team to flush out the mockups, tweaking the design to fit their vision of the new world of program registration.</p>

				<p>The old system was very hierarchical. All the events were organized into a big tree. Users had to navigate the tree to find the program they were looking for. If the program was not where they expected it to be, then Active Living effectively lost a sale.</p>

				<p>The existing programs were tagged, one-by-one, giving each program a virtual descriptor instead of just being a leaf on the tree. The activity engine then organized the programs in the background into logical navigation structures, creating multiple, enticing pathways to finding the courses. </p>

				<div class="portfolio-inline-img">
					<img src="<?=base_url() ?>content/portfolio/activeliving-lrt.png">
					<p class="portfolio-inline-img-caption">
						Programs are organized by similar interest areas. A program can actually belong to many interest areas, so customers are exposed to other course offerings along the path to their program.
					</p>
				</div>

				<p>Active Living hosted several user testing sessions. We pulled students out of the hall, luring them with gift vouchers to test the design. We ran dozens of user experience tests, seeing how well students would navigate the pre-release interface. We tracked their successes and their failures, what they liked and what they didn't. We interated the design, and tested some more.</p>

				<p>The final result resulted in a massive increase in users being able to find programs. The brand new program search filled in the rest of the gaps.</p>

				<p>The two-dozen program coordinators that build and maintain the Active Living program database were trained on the new design and the transition. Two multi-hour training courses were built and executed to bring the staff onboard. Since the design was very data-centric, it was critical that the information was built and maintained properly.</p>


